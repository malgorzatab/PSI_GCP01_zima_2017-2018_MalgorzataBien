	Letter[] inputAlphabet(){
		Letter[]letters = new Letter[20];
		for(int i=0; i<20; i++){
			letters[i] = new Letter();
		}
		
		File file = new File(filename);
		try(BufferedReader br = new BufferedReader(new FileReader(file))){
			
		String line;
		int a=0;
		String[] rows;
		while((line = br.readLine()) !=null && a<20){
			rows = line.split(" ");
			if(rows.length == 35){
				for(String lt : rows){
					if(lt.isEmpty()){
						return null;
					}
				}
			}
			int b=0;
			for(int i=0; i<7; i++){
				for(int j=0; j<5; j++){
					letters[a].setLetter(i,j,Integer.parseInt(rows[b]));
				b++;
				}
			}
			a++;
		}
			
		} catch (FileNotFoundException e) {			
			e.printStackTrace();
		} catch (IOException e) {			
			e.printStackTrace();
		}
		return letters;
	}

	
